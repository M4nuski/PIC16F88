	ASSERTs	0x5678, F2
	
	STRc	0xAABBCC, F3
	STRc	0x112233, F3
	ASSERTc	0x112233, F3
	
	STRi	0xAABBCCDD, F4
	STRi	0x11223344, F4
	ASSERTi	0x11223344, F4
	
	CLRF	F1
	CLRFs	F2
	CLRFc	F3
	CLRFi	F4	
	ASSERTi	0x00000000, F4
	
	STR	0x12, F1
	STRs	0x1234, F2
	STRc	0x123456, F3
	STRi	0x12345678, F4
	
	NOP ;sect 1
	
	MOVLW	0xAA
	NEGw
	ASSERTw	0x56
	NEG	F1
	ASSERTf	-0x12, F1
	NEGs	F2
	ASSERTs	-0x1234, F2
	NEGc	F3
	ASSERTc	-0x123456, F3
	NEGi	F4
	ASSERTi	-0x12345678, F4
	
	MOVLW	0x56
	NEGw
	ASSERTw	0xAA
	NEG	F1
	ASSERTf	0x12, F1
	NEGs	F2
	ASSERTs	0x1234, F2
	NEGc	F3
	ASSERTc	0x123456, F3
	NEGi	F4
	ASSERTi	0x12345678, F4
	
	NOP
	NOP ; sect 2
		
	MOVLW	0x00
	MOVWF	F1
	TESTw
	ASSERTbs	STATUS, Z
	TEST	F1
	ASSERTbs	STATUS, Z
	
	MOVLW	0xFF
	MOVWF	F1
	TESTw	
	ASSERTbc	STATUS, Z
	TEST	F1
	ASSERTbc	STATUS, Z
		
	TESTs	F2
	ASSERTbc	STATUS, Z
	TESTc	F3
	ASSERTbc	STATUS, Z
	TESTi	F4
	ASSERTbc	STATUS, Z
	
	CLRFs	F2
	CLRFc	F3
	CLRFi	F4
	
	TESTs F2
	ASSERTbs	STATUS, Z
	TESTc F3
	ASSERTbs	STATUS, Z
	TESTi F4
	ASSERTbs	STATUS, Z
	
